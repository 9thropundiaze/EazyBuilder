package com.eazybuilder.ga.pojo;

public class PriorityPojo {

    private Integer priority;

    private String name;

    public PriorityPojo(Integer priority, String name) {
        this.priority = priority;
        this.name = name;
    }

    public Integer getPriority() {
        return priority;
    }

    public String getName() {
        return name;
    }
}
